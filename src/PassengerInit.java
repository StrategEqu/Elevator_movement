public class PassengerInit {
    private int weight;
    private int floor;


    public PassengerInit(int weight, int floor){
        this.weight = weight;
        this.floor = floor;
    }

    public int getWeight() {
        return weight;
    }

    public int getFloor() {
        return floor;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public void setFloor(int floor) {
        this.floor = floor;
    }
}